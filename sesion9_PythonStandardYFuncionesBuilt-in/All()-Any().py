# all() y any() Son palabras reservadas de python
# all sirve para verificar que todas las condiciones de la lista se cumplan:
# any sirve para verificar que alguna de las condiciones de la lista se cumplan:
listaA = [ 1==1, 2 == 2, 3 == 4]
print(f'ListaA: {listaA}')
print(f'\n-print(all(listaA)): {all(listaA)}')

print(f'\n-print(any(listaA)): {any(listaA)}')

