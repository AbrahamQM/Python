
a = 5.5
b = 5.4
c = 5.6
numeros = [1, 2, 3, 4, 5, 9, -5]
print("*********Round() redondea datos*********")
print(f'a = {a}, round(a) : {round(a)}')
print(f'b = {b}, round(b) : {round(b)}')
print(f'c = {c}, round(c) : {round(c)}')

print("\n*********Max() y Min() obtienen mínimo y máximo*********")
print(f'numeros = {numeros}, min(numeros) : {min(numeros)}')
print(f'numeros = {numeros}, max(numeros) : {max(numeros)}')

print("\n*********Pow(a, b) elevar datos*********")
print(f'pow(a,b) es similar a poner (a**b)\npow(3, 4) : {pow(3, 4)}')